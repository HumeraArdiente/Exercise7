    private void editbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editbtnActionPerformed
        // TODO add your handling code here:
        int row=searchTable.getSelectedRow();

        Application form = new Application();
        if(row == -1){
            JOptionPane.showMessageDialog(null, "No data selected!");
        }else{
            form.setFirstName(table.getValueAt(row, 1).toString());
            form.setLastName(table.getValueAt(row, 2).toString());
            form.setBirthdate(table.getValueAt(row, 3).toString());
            form.setEmail(table.getValueAt(row, 4).toString());
            form.setContactNum(table.getValueAt(row, 5).toString());
            form.setAddress(table.getValueAt(row, 6).toString());
            if(!table.getValueAt(row, 7).toString().isEmpty()){
                form.setPhoto(table.getValueAt(row, 7).toString());
            }
            form.savebtn.setText("Update");
            form.resetbtn.setText("Delete");
            form.setVisible(true);
        }
    }//GEN-LAST:event_editbtnActionPerformed

